function openTab(evt, tabName) {
    var i, x, tablinks;
    x = document.getElementsByClassName("content-tab");
    for (i = 0; i < x.length; i++) {
        x[i].style.display = "none";
    }
    tablinks = document.getElementsByClassName("tab");
    for (i = 0; i < x.length; i++) {
        tablinks[i].className = tablinks[i].className.replace(" is-active", "");
    }
    document.getElementById(tabName).style.display = "block";
    evt.currentTarget.className += " is-active";

    tree(sizes);
}



function tree(data) {
    for (let cat of data) {
        if (Object.keys(cat)[0] === document.querySelector('.is-active').children[0].innerHTML) {
            list.innerHTML = "";
            for (let i of Object.values(cat)) {
                i.forEach((el, i) => {
                    var p = document.createElement("p");

                    var shape = document.createElement("div");
                    shape.classList.add('shape');

                    var ratio = Math.round((el.width / el.height) * 100) / 100;

                    console.log(el.width, el.height, ratio)

                    var maxWidth = 136;
                    var maxHeight = 136;

                    if (el.width >= el.height) {
                        maxWidth = Math.min(136, el.width / 10);
                        maxHeight = Math.min(el.height / 10, el.height / el.width * 136);
                    } else {
                        maxHeight = Math.min(136, el.height / 10);
                        maxWidth = Math.min(el.width / 10, el.width / el.height * 136);
                    }

                    shape.style.width = maxWidth + el.unit;
                    shape.style.height = maxHeight + el.unit;

                    //   <object type="text/html" data="http://validator.w3.org/"></object>

                    var object = document.createElement("object");
                    object.setAttribute("type", "text/html");
                    object.setAttribute("data", "http://murzyni.com/");
                    object.style.width = "2500px";
                    object.style.height = maxHeight + el.unit;
                    object.style.transform = "scale(.2)";
                    object.style.transformOrigin = "top left";

                    object.style.height = maxHeight * 5 + el.unit;
                    object.style.resize = 'both';

                    var shapeContainer = document.createElement("div");
                    shapeContainer.classList.add('shape-container');

                    shapeContainer.appendChild(shape);
                    shape.appendChild(object);


                    var info = document.createElement("div");
                    info.classList.add('info');

                    var title = document.createElement("h5");
                    title.style.fontWeight = 'bold';

                    var content = document.createElement("div");

                    title.appendChild(document.createTextNode(`${el.name}`));
                    content.appendChild(document.createTextNode(`${el.width} x ${el.height} ${el.unit}`));

                    info.appendChild(title)
                    info.appendChild(content)

                    p.appendChild(shapeContainer)
                    p.appendChild(info)

                    list.appendChild(p);

                })
            }
        }
    }
}

